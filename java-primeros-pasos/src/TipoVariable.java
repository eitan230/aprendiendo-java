
public class TipoVariable {
	
	public static void main(String[] args) {
		System.out.println("Hola");
		
		double caca = 1250.5589;
		
		int caca2 = (int) caca; // Cast, convertir una variable de un tipo en otro
								// En este caso de double (flotante), a int (entero) 
		
		System.out.println(caca2);
		
		long prueba = 1222222222222222222L;
		short numeroPequeño = 13555;
		byte numeroAunMasPequeño = 15;
		float numeroDecimalPequeño = 2.5F;
		
		int resultado = (int) caca + caca2; 
		
	}
	
}
